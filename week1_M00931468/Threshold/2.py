# Area and perimeter of a rectangle
width = float(input('Enter width:'))
height = float(input('Enter height:'))
area = round((width * height),1)
perimeter = round((2 * (width + height)),1)
print(f'\t There is a reactangle of {width} of width with {height} of height. \n It\'s area is {area} and it\' perimeter is {perimeter}')
